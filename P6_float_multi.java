import java.util.Scanner;

public class P6_float_multi {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        float a,b;
        System.out.println("enter the float value for a");
        a = sc.nextFloat();
        System.out.println("enter the float value for b");
        b = sc.nextFloat();
        System.out.println("the result is "+(a*b));
    }
}
